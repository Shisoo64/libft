int	ft_atoi(char *str)
{
	int	rstr;
	int	n;
	int	i;

	i = 0;
	n = 1;
	rstr = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		rstr = rstr * 10 + (str[i] - '0');
		i++;
	}
	return (rstr * n);
}
